			<h3 class="text-primary">SBIRTCoach&#8482 Course Description</h3>
			<p>
				This course trains medical and behavioral health professionals on how to Screen for risky substance use, Briefly Interview about substance use and Refer to Treatment (SBIRT).
			</p>
			<p>
				Diet, exercise, smoking and substance use are the main factors affecting wellness. Substance use may be the one thing you're not asking about. Asking the right questions gives you the right information. Connecting substance use with health, Screening, Brief Intervention and Referral to Treatment (SBIRT) is a small investment with large returns, both in costs and improved health.
			</p>
			<p>
				With SBIRTCoach&#8482, you will learn how to help improve patient outcomes by identifying substance use and determining a patient's level of risk related to alcohol and other drug use. Using patient simulations, guided by natural language processing and artificial intelligence, you will practice how to provide brief interventions about the health impact of substance use and how to effectively refer a patient to treatment when appropriate.
			</p>
			<p>
				With SBIRTCoach™, you will:
			</p>
			<ul>
				<li>earn 3 continuing education, social work or continuing medical education credits</li>
				<li>learn online, on your schedule, 24/7</li>
				<li>simulate real-world situations and conversations</li>
				<li>apply evidence-based screening instruments</li>
				<li>master brief interventions</li>
			</ul>
