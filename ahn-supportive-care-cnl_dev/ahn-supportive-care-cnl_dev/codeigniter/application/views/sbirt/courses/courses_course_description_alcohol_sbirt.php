			<h3 class="text-primary">AlcoholSBIRT&#8482 Course Descriptions</h3>
			<p>
				Alcohol use among active military and veterans is a major health issue in America. Primary care and behavioral health providers are in a unique position to identify risky alcohol use, intervene and refer to treatment.
			</p>
			<p>
				AlcoholSBIRT&#8482 provides both a one and three hour instruction based on curriculum developed by the Defense Center for Excellence and NORC at the University of Chicago.  The one hour course contains a nine module introduction to Screening, Brief Intervention and Referral to Treatment (SBIRT).  In the three hour program, participants practice SBIRT concepts using MedRespond’s virtual coaching in simulated conversations between provider and patient.  AlcoholSBIRT is being used worldwide by the US Army for training primary and behavioral health professionals as part of the Patient Centered Medical Home.
			</p>
			<ul>
				<li>One hour program introducing SBIRT, focus on screening</li>
				<li>Three hour interactive program featuring AUDIT-C and patient role playing
					<ul>
						<li>Offers an opportunity to master brief intervention skills</li>
						<li>Allows users to practice SBIRT techniques using their own words in real-world situations</li>
						<li>Provides immediate feedback and realistic patient response</li>
						<li>Based upon Yale BNI, reflecting VA/DoD clinical practice guidelines</li>
					</ul>
				</li>
				<li>Social Work, CE, CME or APA credit available</li>
				<li>Self-directed and convenient:  complete at work, home or on the road with unlimited 24/7 availability</li>
				<li>Easy to use:  immediate access with a few mouse clicks</li>
				<li>Applies evidence-based screening instruments</li>
				<li>Realistic role playing using active duty, dependent and veteran scenarios</li>
			</ul>
			<p>
				A brief introduction to AlcoholSBIRT may be viewed at <a href="http://www.medrespond.com/case-studies/" target="_blank">https://www.medrespond.com/case-studies/</a>
			</p>

